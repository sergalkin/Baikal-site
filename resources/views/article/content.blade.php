                    <section>
                        <h3>History</h3>
                        <p class="article-p">Lake Baikal was situated in the northern territory of the Xiongnu
                            confederation, and was a site of the Han–Xiongnu War, where the armies of the Han dynasty
                            pursued and defeated the Xiongnu forces from the second century BC to the first century AD.
                            They recorded that the lake was a “huge sea” (hanhai) and designated it the North Sea of the
                            semimythical Four Seas. </p>
                    </section>
                    <section>
                        <h4>The first Russian explorer to reach Lake Baikal was Kurbat Ivanov in
                            1643</h4>
                        <p class="article-p">The Kurykans, a Siberian tribe who inhabited the area in the sixth century,
                            gave it a name that translates to “much water”. Later on, it was called “natural lake”
                            (Baygal nuur) by the Buryats and “rich lake” (Bay gol) by the Yakuts. Little was known to
                            Europeans about the
                            lake until Russia expanded into the area in the 17th century. The first Russian explorer to
                            reach Lake Baikal was Kurbat Ivanov in 1643.</p>
                    </section>
