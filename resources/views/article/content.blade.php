                    <h2>Lake Baikal is the freshwater lake with greatest volume in the world,
                        containing roughly 20%
                        of the world’s unfrozen surface fresh water, and at 1,642 m, the deepest. </h2>
                    <section>
                        <h3>Wildlife</h3>
                        <p class="article-p">In total there are less than 60 native fish species in the lake, but
                            more
                            than half of these
                            are endemic. The families Abyssocottidae (deep-water sculpins), Comephoridae
                            (golomyankas or
                            Baikal oilfish) and Cottocomephoridae (Baikal sculpins), and the subfamily Cottinae
                            (part of
                            the cottid family) are entirely restricted to the lake basin.</p>
                    </section>
                    <section>
                        <h6>INVERTEBRATES</h6>
                        <p class="article-p">The lake hosts rich endemic fauna of invertebrates. Epischura baikalensis
                            is endemic to Lake
                            Baikal and the dominating zooplankton species there, making up 80 to 90% of total
                            biomass.</p>
                    </section>
